    private void conexion(){
        
        String sURL = "jdbc:mysql://localhost:3306/proyectostest";
        try {
            con = DriverManager.getConnection(sURL,"root","");
            System.out.println("Conexion establecida");
        } catch (SQLException ex) {
            Logger.getLogger(PruebaFinal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    private String consultar(String textoconsulta){
        //ResultSet r = null;
        String respuesta = "";
        try (PreparedStatement stmt = con.prepareStatement(textoconsulta)) {
            ResultSet rs = stmt.executeQuery();
            //respuesta=rs.getString(0);
            //System.out.println(respuesta);
            //System.out.print(respuesta + rs.getString("telefono" + rs.getString("fax")));
            //System.out.println("Nombre"+"|\t"+"Telefono"+"|\t"+"Fax");
            while (rs.next())
              respuesta+=rs.getString("nombre")+"\t"+rs.getString("telefono")+"\t"+rs.getString("fax") + "\n";
              //System.out.println(rs.getString("nombre")+"|\t"+rs.getString("telefono")+"|\t"+rs.getString("fax"));

        } catch (SQLException sqle) { 
            System.out.println("Error en la ejecuci√≥n:" 
              + sqle.getErrorCode() + " " + sqle.getMessage());    
        }
        return respuesta;
    }
