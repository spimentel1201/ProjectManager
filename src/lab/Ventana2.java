    void verBaseDatos()
    {
      try
      {
           //Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
           Class.forName("com.mysql.jdbc.Driver").newInstance();
           Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/EmpresaMSQL","root","root");
           //Connection conexion = DriverManager.getConnection("jdbc:odbc:Empresa");
           Statement sentencia = conexion.createStatement();
           //boolean tieneResultados=sentencia.execute(txtSql.getText());
           boolean tieneResultados=sentencia.execute("select * from productos");
           if(tieneResultados)
           {
             ResultSet resultado = sentencia.getResultSet();
             if (resultado != null)
             {
               mostrarResultados(resultado);
             }
             else
             {
               jTextArea1.setText("");
             }
             conexion.close();
           }
      }
      catch(ClassNotFoundException e){
           System.out.println("Controlador no Encontrado: "+e);
      }
      catch(Exception e){
           System.out.println("Error de Conexi√≥n "+e);
      }
    }
    
    void mostrarResultados(ResultSet r) throws SQLException
    {
           ResultSetMetaData res = r.getMetaData(); 
           int numCampos = res.getColumnCount();
           String texto=""; 
           for(int i=1;i<=numCampos;i++)
           {
             if(i<numCampos)
             {
               texto+=res.getColumnName(i)+" \t";
             } else
             {
               texto+=res.getColumnName(i);
             }
           }
           texto+="\n";
           //texto+="-";
           texto+="\t";
           texto+="\n";
           while(r.next())
           {
             for(int j=1;j<=numCampos;j++){
               if(j<=numCampos){
                   texto+=r.getString(j)+" \t";
               } else {
                   texto+=r.getString(j).trim();
               }
             } 
             texto+= "\n";
           }
           jTextArea1.setText(texto);
    }

